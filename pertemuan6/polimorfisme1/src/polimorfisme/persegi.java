package polimorfisme;

/**
 *
 * @author RAFIS
 */
public class persegi extends bangundatar{
    float sisi;

    public persegi(float sisi) {
        this.sisi = sisi;
    }

    @Override
    float luas() {
        return sisi * sisi;
    }

    @Override
    float keliling() {
        return 4 * sisi;
    }
}
