package inheritance;

/**
 *
 * @author RAFIS
 */
public class persegipanjang extends bangundatar {
    double panjang;
    double lebar;

    @Override
    double luas() {
        return panjang * lebar;
    }

    @Override
    double keliling() {
        return 2 * (panjang + lebar);
    }
}
